"""Functions to perform arithmetic operations."""


def add(a, b):
    """Function to add together two numbers. Hello

    Parameters
    ----------
    a: float
        First number of the sum.

    b: float
        Second number of the sum.

    Returns
    -------
    float
        The sum of a and b, a+b.

    """

    return a + b


def multiply(a, b):
    """Function to multiply two numbers.

    Parameters
    ----------
    a: float
        First number of the multiplication.

    b: float
        Second number of the multiplication.

    Returns
    -------
    float
        The product of a and b, a*b.

    """

    return a * b
